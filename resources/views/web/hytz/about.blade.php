            <h1>学院简介</h1>
            <img src="{{url('resources/views/web/hytz/images/9.png')}}" alt="">
            <p>上海火盈投资交易培训学院是香港智富财经投资教育学院在上海设立的交易培训中心。是国内高端金融培训的引领品牌，致力于培养面向金融投资领域的基金经理、分析师、交易员等中高端金融投资人才。</p>
            <p>学院是中国人力资源和社会保障部及中国商务部认可的投资教育机构及考试中心。同时亦是美国IPA国际认证协会授权的《国际注册高级外汇交易师》、《国际注册高级外汇分析师》培训机构和考试中心。多年来我中心面向社会输送了众多优秀交易员和分析师，得到了国内投资机构的一致认可和好评。</p>
            <p>上海火盈投资交易培训学院积极开创行业先河，采用“扎实知识讲解+现场实盘交易训练+岗位从业指导”培训模式，对学员进行“知识+实战”的系统培训，全面提升学员的理论基础及实战操作经验。</p>
            <p>作为国内顶尖的职业交易培训机构，上海火盈投资交易培训学院已与国内外多家知名金融投资公司合作，优秀学员将有机会直接加入这些公司任职。未来上海火盈投资交易培训中心将持续为提高金融行业投资管理水平及发掘和培养优秀投资经理人而继续奋斗，为该行业的持续稳定发展提供充足动力。</p>
